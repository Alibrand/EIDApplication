package com.ksacp2022.electronicinteriordesignapplication.models;

public class Participant {
    String id;
    String name;


    public Participant() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
